    private void jTable_tasksMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_tasksMouseClicked
        // TODO add your handling code here:

        int rowIndex = jTable_tasks.rowAtPoint(evt.getPoint());
        int columnIndex = jTable_tasks.columnAtPoint(evt.getPoint());
        
        Task task = taskTableModel.getTasks().get(rowIndex);
        
        Project project = (Project) projectListModel.get(ListProject.getSelectedIndex());
        
        switch (columnIndex) {
            case 3:
                taskController.update(task);
                break;
            case 4:
                break;
            case 5:
                taskController.removeById(task.getId());
                taskTableModel.getTasks().remove(task);
                loadTasks(project.getId());
                break;
        }

    }//GEN-LAST:event_jTable_tasksMouseClicked

