    public void decorateTableTask() {
        jTable_tasks.getTableHeader().setFont(new Font("Segoe UI Black", Font.PLAIN, 14));
        jTable_tasks.getTableHeader().setBackground(new Color(255, 153, 0));
        jTable_tasks.setAutoCreateRowSorter(true);
        
        jTable_tasks.getColumnModel().getColumn(2).setCellRenderer(new DeadlineColumnCellRenderer());
        
        jTable_tasks.getColumnModel().getColumn(4).setCellRenderer(new ButtonColumnCellRenderer("editTest2"));
        jTable_tasks.getColumnModel().getColumn(5).setCellRenderer(new ButtonColumnCellRenderer("remove2"));
    }
    
    public void initControllers() {
        this.projectController = new ProjectController();
        this.taskController = new TaskController();
    }
    
    public void initComponentsModel() {
        projectListModel = new DefaultListModel();
        loadProjects();
        
        taskTableModel = new TaskTableModel();
        jTable_tasks.setModel(taskTableModel);
        
        if (!projectListModel.isEmpty()) {
            ListProject.setSelectedIndex(0);
            
            Project project = (Project) projectListModel.get(0);
            loadTasks(project.getId());
            
        }
    }
    
    public void loadTasks(int idProject) {
        List<Task> tasks = taskController.getAll(idProject);
        taskTableModel.setTasks(tasks);
        
        showTableTasks(!tasks.isEmpty());
    }
    
    public void loadProjects() {
        List<Project> projects = projectController.getAll();
        
        projectListModel.clear();
        
        for (int i = 0; i < projects.size(); i++) {
            Project project = new Project();
            project = projects.get(i);
            
            projectListModel.addElement(project);
        }
        
        ListProject.setModel(projectListModel);
        
    }
    
    private void showTableTasks(boolean hasTasks) {
        if (hasTasks) {
            //Existem tarefas

            if (EmptyList.isVisible()) {
                EmptyList.setVisible(false);
                jPanel7.remove(EmptyList);
            }
            
            jPanel7.add(jScrollPane_tasks);
            jScrollPane_tasks.setVisible(true);
            jScrollPane_tasks.setSize(jPanel7.getWidth(), jPanel7.getHeight());
            
        } else {
            if (jScrollPane_tasks.isVisible()) {
                jScrollPane_tasks.setVisible(false);
                jPanel7.remove(jScrollPane_tasks);
            }
            
            jPanel7.add(EmptyList);
            EmptyList.setVisible(true);
            EmptyList.setSize(jPanel7.getWidth(), jPanel7.getHeight());
        }
    }
    
